// Tulis function displayUserInfo di sini
function displayUserInfo(nama, umur, kota) {
    console.log(`
        === Info User ===
        Nama: ${nama}
        Umur: ${umur} tahun
        Kota: ${kota}`
    )
}
// Panggil function dengan data berbeda
displayUserInfo("Ahmad", 25, "Jakarta");
displayUserInfo("Lisa", 30, "Bandung");