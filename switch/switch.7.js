let size = "L";
// Tulis kode switch statement di sini
switch (size) {
    case 'S':
        console.log('Size S=Small (36-38)')

        break;
    case 'M':
        console.log('Size M=Medium (40-42)')

        break;
    case 'L':
        console.log('Size L=Large (44-46)')

        break;
    case 'XL':
        console.log('Size XL=Extra Large (48-50)')

        break;

    default:
        console.log('Size tidak tersedia')
        break;
}